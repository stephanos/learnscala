define('app/util/progress',["jquery", "lib/util/underscore"], function($, _) {
  var Progress, p;
  Progress = (function() {

    function Progress() {
      this.init();
      $(document).tooltip({
        delay: {
          show: 500,
          hide: 10
        },
        selector: ".with-tooltip",
        placement: "bottom"
      });
    }

    Progress.prototype.init = function() {
      var _this = this;
      return $(".slidedecks a").each(function(idx, link) {
        var deps, input, key, li, missing, reqs;
        if ($(link).data("id")) {
          li = $(link).parent();
          input = $(li).find("input");
          $(li).removeClass("with-tooltip");
          $(input).unbind();
          li.removeClass("req");
          deps = $(link).data("deps");
          if (deps) {
            deps = deps.split(",");
            missing = _.reject(deps, function(id) {
              return localStorage[_this.getKey(_this.getItem(id).parent().find("input"))];
            });
            if (missing.length > 0) {
              li.addClass("req");
              reqs = "";
              _.each(missing, function(id) {
                return reqs += "<li>" + _this.getItem(id).data("label") + "</li>";
              });
              li.attr("title", "Voraussetzungen: <ul>" + reqs + "</ul>");
              $(li).addClass("with-tooltip");
            }
          }
          $(input).click(_this.update);
          key = _this.getKey(input);
          if (localStorage[key] !== void 0) {
            return _this.apply(input, true);
          }
        }
      });
    };

    Progress.prototype.getItem = function(id) {
      return $("a[data-id='" + id + "']");
    };

    Progress.prototype.reset = function() {
      var key;
      if (confirm('Reset progress - are you sure?')) {
        for (key in localStorage) {
          if (_.str.contains(key, "progress.")) {
            localStorage.removeItem(key);
          }
        }
        return document.location.reload();
      }
    };

    Progress.prototype.update = function(evt) {
      if ($(this).is(':checked')) {
        window.Progress.set($(this), true);
      } else {
        window.Progress.set($(this), void 0);
      }
      return window.Progress.init();
    };

    Progress.prototype.set = function(input, val) {
      var key;
      key = window.Progress.getKey(input);
      localStorage.removeItem(key);
      if (val) {
        this.write(key, true);
      }
      return this.apply(input, val);
    };

    Progress.prototype.apply = function(input, val) {
      var li;
      li = $(input).parent();
      $(li).removeClass("done");
      $(input).attr("checked", false);
      if (val) {
        $(li).addClass("done");
        return $(input).attr("checked", true);
      }
    };

    Progress.prototype.write = function(key, val) {
      console.log("[STORAGE] '" + key + "' = " + val);
      return localStorage[key] = val;
    };

    Progress.prototype.getKey = function(input) {
      return "progress." + $(input).data("key");
    };

    return Progress;

  })();
  p = new Progress;
  window.Progress = p;
  return p;
});


define('app/util/countdown',["jquery", "lib/util/underscore", "lib/util/moment", "app/util/time"], function($, _, moment, Time) {
  var Countdown;
  return Countdown = (function() {

    function Countdown() {
      var self;
      self = this;
      $('<li id="naviCountdown">\
            <span class="divider">|</span>\
            <a href="#" class="openCountdown">\
                <span>Countdown</span>\
            </a>\
          </li>').appendTo($("#navi>ul"));
      $("#naviCountdown").bind("click", function() {
        var m;
        m = $("#countdownModal");
        m.bind("shown", function(evt) {
          var mbody;
          return mbody = $(m).find(".modal-body");
        });
        m.modal();
        return false;
      });
      $('div.countdown').each(function(idx, elem) {
        var key, upd;
        key = "countdown_" + idx;
        upd = function() {
          var diff, end, mm, ms_end, ms_start, now, ss, state, time;
          time = Time.getTime(key);
          ms_end = time[1];
          ms_start = time[0];
          if (ms_start) {
            now = moment();
            end = moment(ms_end);
            state = $(elem).data("state");
            diff = state === "running" ? end.diff(now, 'seconds') : $(elem).data("start");
            $(elem).data("start", diff);
            if (diff < 0) {
              $(elem).addClass("over");
            } else {
              $(elem).removeClass("over");
            }
            mm = diff / 60;
            mm = diff >= 0 ? Math.floor(mm) : Math.ceil(mm);
            $(elem).find(".mm").text(mm);
            ss = Math.abs(diff % 60);
            $(elem).find(".ss").text((ss < 10 ? "0" : "") + ss);
            return state === "running";
          } else {
            return false;
          }
        };
        $(elem).find(".minus").bind("click", function() {
          $(elem).data("start", $(elem).data("start") - 60);
          Time.addTime(key, -60);
          return upd();
        });
        $(elem).find(".plus").bind("click", function() {
          $(elem).data("start", $(elem).data("start") + 60);
          Time.addTime(key, 60);
          return upd();
        });
        return $(elem).find(".toggle").bind("click", function() {
          var state;
          state = $(elem).data("state");
          if (state === "running") {
            return $(elem).data("state", "stopped");
          } else {
            Time.setTime(key, $(elem).data("start"));
            $(elem).data("state", "running");
            return Time.updateClockSchedule(upd, 500);
          }
        });
      });
    }

    return Countdown;

  })();
});
